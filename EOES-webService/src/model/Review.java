package model;

public class Review {
	
	private String reviewId;
	private String reviewTitle;
	private String reviewContent;
	private String userId;
	private String userFullName;
	private String mainImgPath;
	private String mainImgName;
	private String rate;
	private String resId;
	
	/**
	 * @return the resId
	 */
	public String getResId() {
		return resId;
	}
	/**
	 * @param resId the resId to set
	 */
	public void setResId(String resId) {
		this.resId = resId;
	}
	/**
	 * @return the reviewId
	 */
	public String getReviewId() {
		return reviewId;
	}
	/**
	 * @param reviewId the reviewId to set
	 */
	public void setReviewId(String reviewId) {
		this.reviewId = reviewId;
	}
	/**
	 * @return the reviewTitle
	 */
	public String getReviewTitle() {
		return reviewTitle;
	}
	/**
	 * @param reviewTitle the reviewTitle to set
	 */
	public void setReviewTitle(String reviewTitle) {
		this.reviewTitle = reviewTitle;
	}
	/**
	 * @return the reviewContent
	 */
	public String getReviewContent() {
		return reviewContent;
	}
	/**
	 * @param reviewContent the reviewContent to set
	 */
	public void setReviewContent(String reviewContent) {
		this.reviewContent = reviewContent;
	}
	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}
	/**
	 * @return the userFullName
	 */
	public String getUserFullName() {
		return userFullName;
	}
	/**
	 * @param userFullName the userFullName to set
	 */
	public void setUserFullName(String userFullName) {
		this.userFullName = userFullName;
	}
	/**
	 * @return the mainImgPath
	 */
	public String getMainImgPath() {
		return mainImgPath;
	}
	/**
	 * @param mainImgPath the mainImgPath to set
	 */
	public void setMainImgPath(String mainImgPath) {
		this.mainImgPath = mainImgPath;
	}
	/**
	 * @return the mainImgName
	 */
	public String getMainImgName() {
		return mainImgName;
	}
	/**
	 * @param mainImgName the mainImgName to set
	 */
	public void setMainImgName(String mainImgName) {
		this.mainImgName = mainImgName;
	}
	/**
	 * @return the rate
	 */
	public String getRate() {
		return rate;
	}
	/**
	 * @param rate the rate to set
	 */
	public void setRate(String rate) {
		this.rate = rate;
	}
	
	
	
}
